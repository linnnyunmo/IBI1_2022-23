# Define a function to compare the two different sequences
def percentage(seq1, seq2):
    # set initial distance as zero
    edit_distance = 0
    # compare each amino acid
    for i in range(len(seq1)):
        if seq1[i] == seq2[i]:
            # add a score 1 if amino acids are different
            edit_distance += 1
    percentage = edit_distance / len(seq1) * 100  # Calculate the percentage of similarity
    return percentage
