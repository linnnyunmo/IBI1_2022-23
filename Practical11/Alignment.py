# Define a function to calculate the sequence score
def BLOSUM_score(seq1, seq2):
    score = 0
    # Determine the location of amino_acid by x,y
    for i in range(len(seq1)):
        for l in range(len(amino_acid)):
            if seq1[i] == amino_acid[l]:
                x = l
                break
        for l in range(len(amino_acid)):
            if seq2[i] == amino_acid[l]:
                y = l
                break
        # The BLOSUM score between the two sequences
        score = score + matrix[x][y]
    return score


